describe('simple math functions', () => {
    it('verify sum', () => {
        expect(4 + 4).toEqual(8)
    })

    it('verify diff', () => {
        expect(4 - 4).toEqual(0)
    })

    it('verify mult', () => {
        expect(4 * 4).toEqual(16)
    })

    it('verify div', () => {
        expect(4 / 4).toEqual(1)
    })
})